/*模型 3：树形背包（子树状态合并）
场景： 每个节点有体积和价值，且有依赖关系（选了父节点才能选子节点）。给你一个总容量，求最大价值。
工程师解析：
这是背包问题和树形 DP 的结合。本质上是分组背包的思想。

状态定义： dp[u][j] 表示以 u 为根的子树中，分配了 j 的容量所能获得的最大价值。
状态转移： 我们把 u 的每一个子节点 v 看作一个“物品组”。为子节点 v 分配 k 的容量，剩下的 j-k 容量分配给 u 的其他子树。
dp[u][j] = max(dp[u][j], dp[u][j-k] + dp[v][k])

(注：树形背包的循环边界极易写错，且通过巧妙设置循环边界，可以将时间复杂度从 $O(N \cdot V^2)$ 优化到 $O(N \cdot V)$，这是考察工程师基本功的利器。)
模型 4：换根 DP（二次扫描 / Top-down + Bottom-up）
场景： 题目要求出每个节点作为根节点时的答案。如果对每个节点都跑一次 $O(N)$ 的 DFS，总复杂度 $O(N^2)$ 会超时。我们需要 $O(N)$ 的解法。
工程师解析：
这是树形 DP 中的高阶技巧。核心逻辑是 “两次 DFS”：

第一次 DFS (Bottom-up)： 随便选一个节点（比如 1 号点）作为根，跑一次常规的树形 DP，求出以 1 为根时的答案，以及各个子树的局部状态。
第二次 DFS (Top-down)： 从 1 号点开始往下遍历。当我们要把根从父节点 u 换到子节点 v 时，相当于 v 带着它的子树“上位”了，而 u 变成了 v 的一棵子树。
我们利用 u 已经算出的信息，减去 v 原本贡献的信息，再加上 u 作为 v 的子节点带来的新信息，从而在 $O(1)$ 的时间内推导出以 v 为根的答案。
*/