#include<iostream>
#include<vector>

using namespace std;

/*
=============================================================================
【Bellman-Ford 算法核心思想】
作用：解决带有“负权边”的单源最短路问题。
核心：暴力美学。不断尝试用现有的边去“松弛”（缩短）起点到各个点的距离。
性质：一个包含 n 个点的图，任意两点间的最短路径，最多只包含 n-1 条边（除非有负权回路）。
     所以我们最多只需要进行 n-1 轮全局松弛。
=============================================================================
*/

// 规范命名边结构体：u(起点), v(终点), w(权重)
struct Edge {
    int u;
    int v;
    int w;
};
vector<Edge> edges; // 存图方式：由于我们要遍历所有边，直接存入一维数组(vector)即可
int n, m;           // n: 点数, m: 边数
int backup[1005];   // 备份数组：用于保存上一轮的最短距离
int dist[1005];     // 距离数组：保存起点到各个点的当前最短距离
const int INF = 0x3f3f3f3f; 
void bellman(int start) {
    // 【步骤 1：初始化】
    // 将起点到所有点的距离设为无穷大，起点的距离设为 0
    for(int i = 0; i < 1005; i++){
        dist[i] = INF;
    }
    dist[start] = 0;
    // 【步骤 2：外层大循环】
    // 最短路最多包含 n-1 条边，所以我们最多进行 n-1 轮松弛操作
    for(int i = 0; i < n - 1; i++){
       int ret = 0; // 优化标志位：记录本轮是否发生了最短路更新
        
        for(int j = 1; j <= n; j++){
            backup[j] = dist[j];
        }   
        // 【步骤 3：遍历所有边进行“松弛”】
        for(auto edge : edges){
            int r1 = edge.u; // 边的起点
            int r2 = edge.v; // 边的终点
            int r3 = edge.w; // 边的权重  
            // 松弛条件判断：
            // 1. backup[r1] != INF：起点必须是之前已经到达过的点，不能从孤岛出发。
            // 2. dist[r2] > backup[r1] + r3：如果【走到起点的历史最短路 + 这条边的权重】比【当前走到终点的距离】更短
            if(backup[r1] != INF && dist[r2] > backup[r1] + r3){          
                // 核心动作：更新终点的最短距离
                dist[r2] = backup[r1] + r3;        
                ret = 1; // 标记本轮成功发生了更新
            }
        }
        // 如果在某一轮遍历了所有的边，发现没有任何一个点的距离被缩短 (!ret)
        // 说明最短路已经全部找完了，没必要继续跑完 n-1 轮，直接 break 退出，大大提升效率！
        if(!ret){
            break;
        }
    }
    // 如果跑完了 n-1 轮，再悄悄跑第 n 轮，发现还能更新距离 (ret == 1)，
    // 就说明图里存在“负权回路”（转一圈距离越来越小，永远没有最短路）。
}

